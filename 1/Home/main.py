

def task2():
    # 2.Треугольник существует только тогда, когда сумма любых двух его сторон больше третьей.
    # Дано a, b, c - стороны предполагаемого треугольника. Требуется сравнить длину каждого отрезка-стороны с суммой двух других.
    # Если хотя бы в одном случае отрезок окажется больше суммы двух других, то треугольника с такими сторонами не существует.
    # Отдельно сообщить является ли треугольник разносторонним, равнобедренным или равносторонним.

    result = None
    a = float(input("Введите длину первой стороны треугольника: "))
    b = float(input("Введите длину второй стороны треугольника: "))
    c = float(input("Введите длину третьей стороны треугольника: "))

    # Проверяем условие существования треугольника
    if a >= b + c or b >= a + c or c >= a + b:
        result = "Треугольник с такими сторонами не существует"
    else:
        # Проверяем тип треугольника
        if a == b == c:
            result = "Треугольник равносторонний"
        elif a == b or b == c or c == a:
            result = "Треугольник равнобедренный"
        else:
            result = "Треугольник разносторонний"
    return result


